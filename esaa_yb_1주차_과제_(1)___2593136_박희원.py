"""이 때 array1[2]의 타입은 더 이상 ndarray 타입이 아닌 ndarray 내의 데이터값을 의미한다.

*   인덱스에 마이너스 기호를 이용하면 맨 뒤에서부터 데이터를 추출할 수 있다. 인덱스 -1은 맨 뒤의 데이터값을 의미하고, -2는 맨 뒤에서 두 번째에 있는 데이터값이다.


"""

print('맨 뒤의 값:', array1[-1], ' 맨 뒤에서 두 번째 값:', array1[-2])

"""

*   단일 인덱스를 이용해 ndarray 내의 데이터값도 간단히 수정할 수 있다.

"""

array1[0] = 9
