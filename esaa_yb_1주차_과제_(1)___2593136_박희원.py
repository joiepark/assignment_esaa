print(zero_array.dtype, zero_array.shape)

one_array = np.ones((3, 2))
print(one_array)
print(one_array.dtype, one_array.shape)

"""### ndarray의 차원과 크기를 변경하는 reshape()



*   reshape() 메서드는 ndarray를 특정 차원 및 크기로 변환한다. 변환을 원하는 크기를 함수 인자로 부여하면 된다.



"""

array1 = np.arange(10)
print('array1:\n', array1)

array2 = array1.reshape(2, 5)
print('array2:\n', array2)

array3 = array1.reshape(5, 2)
print('array3:\n', array3)

"""

*   reshape()는 지정된 사이즈로 변경이 불가능하면 오류를 발생시킨다. 가령 (10,) 데이터를 (4,3) Shape 형태로 변경할 수는 없는 것이다.

"""

array1.reshape(4, 3)

"""

*   reshape()를 실전에서 더욱 효율적으로 사용하는 경우는 아마도 인자로 -1을 적용하는 경우일 것이다. -1을 인자로 사용하면 원래 ndarray와 호환되는 새로운 shape로 변환해준다. 물론 -1을 사용하더라도 호환될 수 없는 형태는 변환할 수 없다.

"""

array1 = np.arange(10)
print(array1)
array2 = array1.reshape(-1, 5)
print('array2 shape:', array2.shape)
