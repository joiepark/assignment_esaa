print('array1:', array1)

"""

*   다차원 ndarray에서 단일 값을 추출할 때, 3차원 이상의 ndarray에서의 데이터 추출은 2차원 ndarray와 큰 차이가 없다. 1차원과 2차원 ndarray에서의 데이터 접근의 차이는 2차원의 경우 콤마(,)로 분리된 로우와 칼럼 위치의 인덱스를 통해 접근하는 것이다.

"""

# 1차원 ndarray를 2차원 3 x 3 ndarray로 변환 후, [row, col]을 이용해 2차원 ndarray에서의 데이터 추출
array1d = np.arange(start=1, stop=10)
array2d = array1d.reshape(3, 3)
print(array2d)

print('(row=0, col=0) index 가리키는 값:', array2d[0, 0])
print('(row=0, col=1) index 가리키는 값:', array2d[0, 1])
print('(row=1, col=0) index 가리키는 값:', array2d[1, 0])
print('(row=2, col=2) index 가리키는 값:', array2d[2, 2])

"""

*   넘파이의 다차원 ndarray는 axis 구분을 가진다. axis 0은 로우 방향의 축을 의미하고, axis 1은 칼럼 방향의 축을 의미한다. 정확히 얘기하자면, 앞 설명에서 지칭한 로우와 칼럼은 넘파이 ndarray에서는 사용되지 않는 방식이다. 이해를 돕기 위해 로우, 칼럼 형식으로 표현한 것일 뿐 정확한 표현은 axis 0, axis 1이 맞다. 즉 [row=0, col=1] 인덱싱은 [axis0=0, axis1=1]이 정확한 표현이다. 2차원이므로 axis 0, axis 1로 구분되는 것이며 3차원 ndarray의 경우에는 axis 0, axis 1, axis 2로 3개의 축을 가지게 된다. (행, 열, 높이로 이해하면 된다.)
*   슬라이싱은 ':' 기호를 이용해 연속한 데이터를 추출하는 기능이다. 단일 데이터값 추출을 제외하고 슬라이싱, 팬시 인덱싱, 불린 인덱싱으로 추출된 데이터 세트는 모두 ndarray 타입이다. ':' 사이에 시작 인덱스와 종료 인덱스를 표시하면 시작 인덱스에서 종료 인덱스-1의 위치에 있는 데이터의 ndarray를 반환한다.
"""

array1 = np.arange(start=1, stop=10)
array3 = array1[0:3]
print(array3)
