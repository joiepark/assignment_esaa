print('array3 shape:', array3.shape)

array1 = np.arange(10)
array4 = array1.reshape(-1, 4)

"""

*   -1 인자는 reshape(-1,1)와 같은 형태로 자주 사용된다. reshape(-1,1)은 원본 ndarray가 어떤 형태라도 2차원이고, 여러 개의 로우를 가지되 반드시 1개의 컬럼을 가진 ndarray로 변환됨을 보장한다. 여러 개의 넘파이 ndarray는 stack이나 concat으로 결합할 때 각각의 ndarray의 형태를 통일해 유용하게 사용된다.
*   ndarray는 tolist() 메서드를 이용해 리스트 자료형으로 변환할 수 있다.

"""

