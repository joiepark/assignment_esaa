import numpy as np

"""물론 import numpy만 해도 충분하지만, as np를 추가해 약어로 모듈을 표현해주는 게 관례이다.


*   넘파이의 기반 데이터 타입은 ndarray이며, 이를 이용해 넘파이에서 다차원(Multi-dimension) 배열을 쉽게 생성하고 다양한 연산을 수행할 수 있다.
*   넘파이 array()함수는 파이썬의 리스트와 같은 다양한 인자를 입력받아서 ndarray로 변환하는 기능을 수행한다. 생성된 ndarray 배열의 shape 변수는 ndarray의 크기, 즉 행과 열의 수를 튜플 형태로 가지고 있으며 이를 통해 ndarray 배열의 차원까지 알 수 있다.


"""

