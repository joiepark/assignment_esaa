print(array2, array2.dtype)

list3 = [1, 2, 3.0]
array3 = np.array(list3)
print(array3, array3.dtype)

"""int형 값과 문자열이 섞여있는 list2를 ndarray로 변환한 array2는 숫자형 값 1, 2가 모두 문자열 값인 '1', '2'로 변환됐다. (int형이 유니코드 문자열 값으로 변환됐다.)
int형과 float형이 섞여있는 list3의 경우에도 int 1, 2가 모두 1. 2.인 float 64형으로 변환됐다.


*   ndarray 내 데이터값의 타입 변경도 astype() 메서드를 이용해 할 수 있다. astype()에 인자로 원하는 타입을 문자열로 지정하면 된다. 이렇게 데이터 타입을 변경하는 경우에는 대용량 데이터의 ndarray를 만들 때 많은 메모리가 사용되는데 메모리를 더 절약해야 할 때 보통 이용된다. 대용량의 데이터를 로딩할 때는 수행속도가 느려지거나 메모리 부족으로 오류가 발생할 수 있기 때문이다.

"""

array_int = np.array([1, 2, 3])
